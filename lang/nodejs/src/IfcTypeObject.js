/**
 * http://www.buildingsmart-tech.org/ifc/IFC4/final/html/link/ifctypeobject.htm
 */
module.exports = class IfcTypeObject extends IfcObjectDefinition {
  
    constructor(globalId) {
        super(globalId)
        	this.ApplicableOccurrence = null  // optional
	this.HasPropertySets = null  // optional
	this.Types = null  // inverse

    }
    getStepParameters() {
        const parameters = []
    		parameters.push(BaseIfc.toStepValue(this.GlobalId))
		parameters.push(BaseIfc.toStepValue(this.OwnerHistory))
		parameters.push(BaseIfc.toStepValue(this.Name))
		parameters.push(BaseIfc.toStepValue(this.Description))
		parameters.push(BaseIfc.toStepValue(this.ApplicableOccurrence))
		parameters.push(BaseIfc.toStepValue(this.HasPropertySets))

        return parameters.join()
    }
}