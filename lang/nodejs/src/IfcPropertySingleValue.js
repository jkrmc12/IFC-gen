/**
 * http://www.buildingsmart-tech.org/ifc/IFC4/final/html/link/ifcpropertysinglevalue.htm
 */
module.exports = class IfcPropertySingleValue extends IfcSimpleProperty {
  
    constructor(name) {
        super(name)
        	this.NominalValue = null  // optional
	this.Unit = null  // optional

    }
    getStepParameters() {
        const parameters = []
    		parameters.push(BaseIfc.toStepValue(this.Name))
		parameters.push(BaseIfc.toStepValue(this.Description))
		parameters.push(BaseIfc.toStepValue(this.NominalValue))
		parameters.push(BaseIfc.toStepValue(this.Unit))

        return parameters.join()
    }
}