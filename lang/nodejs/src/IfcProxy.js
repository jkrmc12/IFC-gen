/**
 * http://www.buildingsmart-tech.org/ifc/IFC4/final/html/link/ifcproxy.htm
 */
module.exports = class IfcProxy extends IfcProduct {
  
    constructor(globalId, proxyType) {
        super(globalId)
		this.ProxyType = proxyType

        	this.Tag = null  // optional

    }
    getStepParameters() {
        const parameters = []
    		parameters.push(BaseIfc.toStepValue(this.GlobalId))
		parameters.push(BaseIfc.toStepValue(this.OwnerHistory))
		parameters.push(BaseIfc.toStepValue(this.Name))
		parameters.push(BaseIfc.toStepValue(this.Description))
		parameters.push(BaseIfc.toStepValue(this.ObjectType))
		parameters.push(BaseIfc.toStepValue(this.ObjectPlacement))
		parameters.push(BaseIfc.toStepValue(this.Representation))
		parameters.push(BaseIfc.toStepValue(this.ProxyType))
		parameters.push(BaseIfc.toStepValue(this.Tag))

        return parameters.join()
    }
}